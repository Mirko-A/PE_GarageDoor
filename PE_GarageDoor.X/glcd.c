/*-------------------------------------------------------------------------------
Wait as long as the LCD is busy
	LcdWaitBusy();
-------------------------------------------------------------------------------*/
void LcdWaitBusy (void)
{
	unsigned char temp;
	SetRS(0);       	     /* Instruction mode */
  	SetRW(1);       	     /* Read mode */
  	
	ConfigureLcdData(INPUT); /* set LCD_DATA port in input mode */
	SetE(1); 
	temp = ReadLcdData();
	SetE(0); 
	
	while (temp & 0x80) 
	{
		temp=ReadLcdData();
	}
}

